<script>
import { mapState } from "vuex";
// 引入方式:把lodash全部功能都引入(不太建议)
// import _ from 'lodash'
// 最好的引入方式: 按需引入
import throttle from "lodash/throttle"; // 从源码可以看出,throttle是默认暴露的
// import debounce from 'lodash/debounce'  // 从源码可以看出,throttle是默认暴露的
export default {
  name: "TypeNav",
  data() {
    return {
      currentIndex: -1,
      show: true,
    };
  },
  methods: {
    tiaoshi() {
      alert(index === this.currentIndex);
    },
    // changeIndex(index){
    // },
    // throttle是lodash的一个函数,节流函数
    // 注意: throttle的回调函数不要使用箭头函数
    changeIndex: throttle(function (index) {
      this.currentIndex = index;
      // 正常情况（用户操作的慢）：鼠标进入，每一个一级分类h3，都会触发鼠标进入事件
      // 非正常情况（用户操作过快）：本身全部的一级分类都应该触发鼠标进入事件，但是经过测试，只有部分h3触发
      // 就是由于用户操作过快,导致浏览器反应不过来,如果当前回调函数中有一些大量业务,有可能出现卡顿现象
    }, 50),
    goSearch(event) {
      /*
                    最好的解决方法:编程式导航 + 事件委派
                    存在一些问题:事件委派,把全部的子节点的事件委派给父节点
                    点击a标签的时候,才会进行路由跳转[问题是怎么能确定点击的一定是a标签]
                    同时还存在另一个问题:即使你能确定点击的是a标签,如何区分是一级,二级,三级分类的标签
                */

      // 第一个问题:把子节点当中的a标签加上自定义属性data-categoryName,其余的子节点是没有这个属性的
      let element = event.target;
      // 获取到当前出发这个事件的节点[h3,a,dt,dl],而带有data-categoryName这样节点的一定是a标签
      // 节点有一个属性dataset属性，可以获取到节点的自定义属性和属性值
      let { categoryname, category1id, category2id, category3id } =
        element.dataset;
      // console.log(categoryname);
      if (categoryname) {
        // 整理路由跳转的参数
        let location = { name: "search" };
        let query = { categoryName: categoryname };
        if (category1id) {
          query.category1Id = category1id;
        } else if (category2id) {
          query.category2Id = category2id;
        } else {
          query.category3Id = category3id;
        }
        // 整理一下参数
        location.query = query;

        // 判断：如果路由跳转的时候，带有params参数，也要传递过去
        if (this.$route.params) {
          // 其实这个判断没起作用，因为一个空的对象映射到布尔值也为true
          location.params = this.$route.params;
          location.query = query;
          // 路由跳转
          this.$router.push(location);
        }
      }
    },
    enterShow() {
      this.show = true;
    },
    leaveShow() {
      this.currentIndex = -1;
      if (this.$route.path != "/home") {
        this.show = false;
      }
    },
  },
  // 组件挂载完毕，可以向服务器发请求
  mounted() {
    if (this.$route.path != "/home") {
      this.show = false;
    }
  },
  computed: {
    ...mapState({
      categoryList: (state) => state.home.categoryList,
    }),
    // ...mapState('home', ['categoryList']),
  },
};
</script>

